# Set up our years
current_year = 2025
future_year = 2050

# Ask for current age and turn it into a number
age = int(input("How old are you now? "))

# Calculate how many years to wait
years_to_wait = future_year - current_year

# Calculate future age
future_age = age + years_to_wait

# Show the result!
print("In the year 2050, you will be:")
print(future_age)

