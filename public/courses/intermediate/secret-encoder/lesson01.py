# ord() turns a letter into its secret number
letter = "A"
code = ord(letter)
print("The letter A is secretly:")
print(code)

print("")

# chr() turns a number back into a letter
number = 66
letter_b = chr(number)
print("The number 66 is secretly:")
print(letter_b)

