# Length converters
def cm_to_inches(cm):
    return cm / 2.54

def meters_to_feet(m):
    return m * 3.281

# Weight converters
def kg_to_pounds(kg):
    return kg * 2.205

# Test them!
print("150 cm =", cm_to_inches(150), "inches")
print("2 meters =", meters_to_feet(2), "feet")
print("70 kg =", kg_to_pounds(70), "pounds")

